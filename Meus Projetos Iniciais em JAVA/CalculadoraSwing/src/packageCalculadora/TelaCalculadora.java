    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int numero = (int) jsNumero.getValue();
        
        float resto, cubo, raizQ, raizC, valorAbs;
        
        resto = numero % 2;
        cubo = (float) Math.pow(numero, 3);
        raizQ = (float) Math.sqrt(numero);
        raizC = (float) Math.cbrt(numero);
        valorAbs = (float) Math.abs(numero);
        
        lblResto.setText(Float.toString(resto));
        lblCubo.setText(Float.toString(cubo));
        lblRaiz.setText(Float.toString(raizQ));
        lblCubica.setText(Float.toString(raizC));
        lblAbs.setText(Float.toString(valorAbs));
    }//GEN-LAST:event_jButton1ActionPerformed

